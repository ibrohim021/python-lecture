# Словари

color = {'red', 'green', 'blue'}


print(color)
print()

print('Добавили элемеинт 5')
color.add('5')
print(color)
print()

print('Удалии элемеинт 5')
color.remove('5')
print(color)
print()

print('Исключаем красный цвет')
color.discard('red')
print(color)
print()

print('Очищаем список')
color.clear()
print(color)